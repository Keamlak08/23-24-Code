public class Casting{
   public static void main(String[]args ){
   
      //float twoThirds = 2/3
            
      float twoThirdsTakeTwo = (float)2/3;
      System.out.println(twoThirdsTakeTwo);
      
      double twoThirdsTakeThree = 2.0/3.0;
      System.out.println(twoThirdsTakeThree);
      
      float twoThirdsTakeFour = 2f/3;
      System.out.println(twoThirdsTakeFour);
      
      
   }   
}